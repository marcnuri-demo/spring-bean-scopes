package com.marcnuri.demo.springbeanscopes;

import java.io.Serializable;
import java.util.UUID;

/**
 * Created by Marc Nuri <marc@marcnuri.com> on 2017-11-15.
 */
public class Sample implements Serializable{

	private static final long serialVersionUID = -1953504228661239875L;

//**************************************************************************************************
//  Fields
//**************************************************************************************************
	private final UUID uuid;

//**************************************************************************************************
//  Constructors
//**************************************************************************************************
	public Sample() {
		this.uuid = UUID.randomUUID();
	}

//**************************************************************************************************
//  Abstract Methods
//**************************************************************************************************

//**************************************************************************************************
//  Overridden Methods
//**************************************************************************************************
	@Override
	public boolean equals(Object o) {
		if (this == o) return true;
		if (o == null || getClass() != o.getClass()) return false;

		Sample sample = (Sample) o;

		return uuid != null ? uuid.equals(sample.uuid) : sample.uuid == null;
	}

	@Override
	public int hashCode() {
		return uuid != null ? uuid.hashCode() : 0;
	}

//**************************************************************************************************
//  Other Methods
//**************************************************************************************************

//**************************************************************************************************
//  Getter/Setter Methods
//**************************************************************************************************
	public UUID getUuid() {
		return uuid;
	}

//**************************************************************************************************
//  Static Methods
//**************************************************************************************************

//**************************************************************************************************
//  Inner Classes
//**************************************************************************************************

}
